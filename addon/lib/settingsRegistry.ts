const registryMap = new Map<string, SettingDefinition>();
for (const def of SETTINGS_REGISTRY) {
  registryMap.set(def.key, def);
}

export function getSettingDefinition(key: string): SettingDefinition | undefined {
  return registryMap.get(key);
}

export function getSettingsByCategory(): Map<string, SettingDefinition[]> {
  const categories = new Map<string, SettingDefinition[]>();
  for (const def of SETTINGS_REGISTRY) {
    const list = categories.get(def.category) || [];
    list.push(def);
    categories.set(def.category, list);
  }
  return categories;
}
